let web3;
let contract;

async function connectWallet() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });
        contract = new web3.eth.Contract(contractABI, contractAddress);
        alert("Wallet connected");
    } else {
        alert("Please install MetaMask");
    }
}

async function registerArtist() {
    const accounts = await web3.eth.getAccounts();
    const name = document.getElementById("artistName").value;
    const profileHash = document.getElementById("artistProfile").value;
    await contract.methods.registerArtist(name, profileHash).send({ from: accounts[0] });
    alert("Artist Registered");
}

async function uploadTrack() {
    const accounts = await web3.eth.getAccounts();
    const title = document.getElementById("trackTitle").value;
    const ipfsHash = document.getElementById("trackIPFS").value;
    const metadataHash = document.getElementById("metadataHash").value;
    const price = document.getElementById("trackPrice").value;
    const genre = document.getElementById("trackGenre").value;
    await contract.methods.uploadTrack(title, ipfsHash, metadataHash, price, genre).send({ from: accounts[0] });
    alert("Track Uploaded");
}

async function streamTrack() {
    const accounts = await web3.eth.getAccounts();
    const trackId = document.getElementById("trackId").value;
    const price = await contract.methods.getTrackDetails(trackId).call();
    await contract.methods.streamTrack(trackId).send({ from: accounts[0], value: price.price });
    alert("Track Streamed");
}
